#include "libft.h"

t_bool	ft_strcmp(char *s1, char *s2)
{
	int		i;
	t_bool	result;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			break ;
		}
		i += 1;
	}
	result = s1[i] - s2[i];
	return (result);
}
